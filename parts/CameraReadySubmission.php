    Note: The following instructions are adapted from ICC2019 Steering Committee.

The deadline is August 1st, 2019. Failure to submit your Camera-Ready files by this date will result in your paper being not included in the proceeding. You may submit the final version of your paper by navigating HERE and following the instructions.

Any changes you see fit to make to the final version of your paper as well as suggestions made by the reviewers should be incorporated in the camera-ready version. In addition, please be sure to do all of the following:
•	Ensure that your paper conforms to the required format (see the Springer templates).
•	Include the authors' names and affiliations under the title. Note that the list of authors should be identical to the list specified when submitting the paper.
•	Ensure that your paper conforms to the appropriate submission types, 12-15 pages for long papers and 6-8 pages for short papers, including all tables, figures, and references.
•	Ensure that all tables and figures are at a high resolution.
•	If you have supplemental material, please read the following: Supplemental material can be divided into two types: appendices and non-readable supplemental material:
o	Appendices are material that can be read, and include lemmas, formulas, proofs, and tables that are not critical to the reading and understanding of the paper. In your final camera-ready paper, appendices come after the references in the main paper (see the Springer templates). Appendices do not count towards the page limit.
o	Non-readable supplemental material should follow the Springer templates.
•	The file (Camera Ready copy) must be in Portable Document Format (PDF) on A4 paper.
•	All figures and tables must be included directly in the paper in their final positions, and at a resolution that is both readable when printed or when viewed on an electronic device in PDF format.

•	Authors are required to send a zipped file, containing the following:
1.	Camera-ready paper in a pdf format.
2.	Camera-ready paper source file (Ms_Word or Latex).  
3.	Completed copyright form. 

